# ── Email with attachment ─────────────────────────────────────────────────────

def _send_report_email(to_list: List[str], subject: str, body_html: str, filepath: str) -> bool:
    """Send report email with file attachment."""
    if os.getenv("EMAIL_ENABLED", "false").lower() != "true":
        logger.info(f"[PRISM Reports] Email disabled — would have sent report to {to_list}")
        return False

    smtp_host = os.getenv("SMTP_HOST", "smtp.gmail.com")
    smtp_port = int(os.getenv("SMTP_PORT", 587))
    smtp_user = os.getenv("SMTP_USERNAME", "")
    smtp_pass = os.getenv("SMTP_PASSWORD", "")
    smtp_from = os.getenv("SMTP_FROM", smtp_user)

    try:
        msg = MIMEMultipart()
        msg["Subject"] = subject
        msg["From"] = smtp_from
        msg["To"] = ", ".join(to_list)
        msg.attach(MIMEText(body_html, "html"))

        # Attach the report file
        if filepath and os.path.exists(filepath):
            filename = os.path.basename(filepath)
            with open(filepath, "rb") as f:
                part = MIMEBase("application", "octet-stream")
                part.set_payload(f.read())
            encoders.encode_base64(part)
            part.add_header("Content-Disposition", f"attachment; filename={filename}")
            msg.attach(part)

        with smtplib.SMTP(smtp_host, smtp_port) as server:
            server.ehlo()
            server.starttls()
            server.login(smtp_user, smtp_pass)
            server.sendmail(smtp_from, to_list, msg.as_string())

        logger.info(f"[PRISM Reports] Report emailed to {to_list}")
        return True

    except Exception as e:
        logger.error(f"[PRISM Reports] Email failed: {e}")
        return False


